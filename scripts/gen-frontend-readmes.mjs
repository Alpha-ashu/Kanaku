#!/usr/bin/env node
// Generates a README.md for each top-level frontend/src folder + a master
// architecture map that maps the actual structure to the §2 conceptual model.
// Run from the repo root: `node scripts/gen-frontend-readmes.mjs`.
import fs from 'node:fs';
import path from 'node:path';

const SRC = path.resolve('frontend/src');

const PURPOSE = {
  app: 'Primary application UI: `App.tsx` root + the full component tree. Most route screens and feature UIs live under `app/components/*` (see catalog below).',
  assets: 'Static assets (images, etc.) imported by components.',
  components: 'Top-level shared components mounted outside the `app/` tree (voice assistant, notification UI, sync-status indicators).',
  config: 'Frontend runtime configuration.',
  constants: 'App-wide constant data (navigation, banks, categories).',
  contexts: 'React Context providers — the client-side state layer (Auth, App, Security). This is the app’s §2 "store".',
  hooks: 'Reusable React hooks (RBAC, permissions, device sync, feature flags, receipt scanner, scroll).',
  lib: 'Core libraries & data layer: API client, Dexie/IndexedDB database, sync engines, encryption, notifications, PWA, currency/date utilities.',
  pages: 'Route-level page wrappers. NOTE: most route screens currently live under `app/components/*` (core, features, marketing); this folder is sparse.',
  services: 'Client-side business/domain services (AI/intelligence, OCR & receipts, voice, statement import, permissions, transactions).',
  strategies: 'Pluggable strategy implementations (parsing/categorization strategies).',
  styles: 'Global stylesheets / Tailwind layers.',
  types: 'Shared TypeScript type definitions.',
  utils: 'Generic helper utilities.',
};

const listing = (dir) => {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  const dirs = entries.filter((e) => e.isDirectory()).map((e) => `${e.name}/`).sort();
  const files = entries.filter((e) => e.isFile()).map((e) => e.name).sort();
  const count = (d) => {
    let n = 0;
    for (const e of fs.readdirSync(d, { withFileTypes: true })) {
      const p = path.join(d, e.name);
      if (e.isDirectory()) n += count(p);
      else if (/\.(ts|tsx)$/.test(e.name)) n += 1;
    }
    return n;
  };
  return { dirs, files, count: count(dir) };
};

const topDirs = fs
  .readdirSync(SRC, { withFileTypes: true })
  .filter((e) => e.isDirectory())
  .map((e) => e.name)
  .sort();

const rows = [];
for (const name of topDirs) {
  const dir = path.join(SRC, name);
  const { dirs, files, count } = listing(dir);
  const purpose = PURPOSE[name] || '_TODO: describe this folder._';

  const subdirsBlock = dirs.length ? dirs.map((d) => `- \`${d}\``).join('\n') : '_None._';
  const filesBlock = files.length
    ? files.slice(0, 18).map((f) => `- \`${f}\``).join('\n') + (files.length > 18 ? `\n- … (${files.length - 18} more)` : '')
    : '_None._';

  const readme = `# ${name}/

> ${purpose}

**Contents:** ${count} \`.ts/.tsx\` files.

## Subfolders

${subdirsBlock}

## Files

${filesBlock}

---
_Auto-generated by \`scripts/gen-frontend-readmes.mjs\` (\`npm run docs:frontend\`). Edit the purpose map in the generator, not here._
`;
  fs.writeFileSync(path.join(dir, 'README.md'), readme);
  rows.push({ name, purpose, count, subdirs: dirs.length });
}

// Catalog of app/components/* (where the features live)
let featureCatalog = '_app/components not found._';
const appComponents = path.join(SRC, 'app', 'components');
if (fs.existsSync(appComponents)) {
  featureCatalog = fs
    .readdirSync(appComponents, { withFileTypes: true })
    .filter((e) => e.isDirectory())
    .map((e) => e.name)
    .sort()
    .map((f) => `\`${f}\``)
    .join(' · ');
}

const catalogRows = rows
  .map((r) => `| [\`${r.name}/\`](./${r.name}/README.md) | ${r.count} | ${r.purpose.replace(/\|/g, '\\|')} |`)
  .join('\n');

const master = `# Frontend source (\`frontend/src\`)

A React + Vite + Dexie (offline-first) SPA. This map lets you understand the
layout without reading code. Imports use the \`@\` alias → \`frontend/src\`.

## Top-level folders

| Folder | Files | Purpose |
|---|---|---|
${catalogRows}

## Where the features live

The bulk of the UI is under \`app/components/*\`, one folder per domain:

${featureCatalog}

## Mapping to the target structure (§2)

| §2 concept | Where it lives today | Status |
|---|---|---|
| pages/ | \`pages/\` (sparse) + route screens under \`app/components/{core,features,marketing}\` | partial |
| features/ | \`app/components/{accounts,goals,groups,investments,loans,transactions,…}\` | present (named app/components) |
| components/ | \`app/components/ui\` (design system) + \`app/components/shared\` + top-level \`components/\` | present |
| services/ | \`services/\` + data/API in \`lib/\` | present |
| hooks/ | \`hooks/\` | present |
| store/ | \`contexts/\` (React Context state) | present (as contexts) |
| schemas/ | — (validation is inline / in \`lib\`) | **gap** |
| constants/ | \`constants/\` + \`app/constants\` | present |
| docs/ | this README set | present |
| tests/ | co-located \`*.test.ts(x)\` in \`services/\`, \`lib/\` | present (co-located) |

**Tracked gaps:** no dedicated \`schemas/\` folder (consider extracting shared Zod
schemas); \`pages/\` is sparse because route screens live under \`app/components\`.

---
_Auto-generated by \`scripts/gen-frontend-readmes.mjs\`. Run \`npm run docs:frontend\` after adding/removing top-level folders._
`;

fs.writeFileSync(path.join(SRC, 'README.md'), master);
console.log(`Generated ${rows.length} folder READMEs + frontend/src master map.`);
